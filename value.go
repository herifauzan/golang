package main

import "fmt"
	
func main() {
//konkatenasi dua nilai string
    fmt.Println("go" + "lang")
//string di konkatenasi dengan hasil hitung integer
    fmt.Println("1+1 =", 1+1)
//hasil real dibagi real adalah bilangan real
    fmt.Println("7.0/3.0 =", 7.0/3.0)
//print hasil bilangan logika/boolean
    fmt.Println(true && false)
    fmt.Println(true || false)
    fmt.Println(!true)
}
